package com.mao.buildmode;

/**
 * @author mao
 * @date 2021-06-26
 * description 人物对象
 */
public class Person {
    /**
     * 头部
     */
    private String head;
    /**
     * 体部
     */
    private String body;

    /**
     * 尾部
     */
    private String foot;

    public String getHead() {
        return head;
    }

    public void setHead(String head) {
        this.head = head;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getFoot() {
        return foot;
    }

    public void setFoot(String foot) {
        this.foot = foot;
    }
}
